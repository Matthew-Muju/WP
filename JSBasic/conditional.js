// if/else
let prodi = 3;
if(prodi === 1){
    console.log('Sistem Informasi');
}
else if(prodi === 2){
    console.log('Informatika');
}
else {
    console.log('Not FIK');
}

//swicth

switch(prodi){
    case 1:
        console.log('Sistem Informasi');
        break;
    case 2:
        console.log('Informatika');
        break;
    default:
        console.log('Not Fik');
        break;        
}